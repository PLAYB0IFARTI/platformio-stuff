bool button_pressed = false;
const int ideality = 3;
const int delay_num = 100;
String phrases[ideality] = {"2 + 2 = 5", "i love meursault", "edward sucks"};
String phrase, obscured;

Adafruit_SSD1306 display(128, 64, &Wire, -1);

// generates the random stuff until the button is pressed
void generate() {
  for (int i = 0; i < phrase.length(); i++) {
    long rand = random(33, 126);
    obscured[i] = rand;
  }
  display.clearDisplay();
  display.setCursor(0,0);
  display.drawBitmap(0, 0, epd_bitmap_index, 128, 64, SSD1306_WHITE);
  display.setCursor(0,0);
  display.println(obscured);
  display.display();
  delay(delay_num);
}

// reveals the prescript
void reveal() {
  for (int i = 0; i < phrase.length(); i++) {
    for (int j = i; j < phrase.length(); j++) {
      long rand = random(33,126);
      obscured[j] = rand;
    }
    obscured[i] = phrase[i];

    display.clearDisplay();
    display.setCursor(0,0);
    display.drawBitmap(0, 0, epd_bitmap_index, 128, 64, SSD1306_WHITE);
    display.setCursor(0,0);
    display.println(obscured);
    display.display();
    delay(delay_num);
  }
  delay(1000);
  phrase = phrases[random(0, ideality)];
  obscured = phrase;
  button_pressed = false;
}

void setup() {
  Serial.begin(115200);
  pinMode(18, INPUT_PULLDOWN);
  randomSeed(analogRead(2));

  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    while (true) {}
  }

  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);
  display.setCursor(0,0);
  display.setTextSize(1);
  display.display();
  phrase = phrases[random(0, ideality)];
  obscured = phrase;
}

void loop() {
  generate();
  if (digitalRead(18) && !button_pressed) {
    reveal();
  }
}
