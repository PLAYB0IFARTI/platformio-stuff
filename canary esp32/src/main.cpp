void canary() {
  bme.performReading();
  int current_gas_res = bme.gas_resistance / 1000;
  int gaschange = avg_gas - current_gas_res;
  display.clearDisplay();
  display.setCursor(X,Y);
  display.setTextSize(1);
  if (gaschange < 20) {
	display.drawBitmap(0, 0, epd_bitmap_1_healthy, 128, 64, WHITE);
	// serials were to debug
	Serial.println("Good");
  }
  if (20 <= gaschange && gaschange < 25) {
	display.drawBitmap(0, 0, epd_bitmap_2_uncomfy, 128, 64, WHITE);
	Serial.println("Not good");
	ledcWriteTone(0, 1000);
  	delay(100);
  }
  if (25 <= gaschange && gaschange < 30) {
	display.drawBitmap(0, 0, epd_bitmap_3_nothealthy, 128, 64, WHITE);
	Serial.println("Bad");
	ledcWriteTone(0, 1000);
  	delay(200);
  }
  if (30 <= gaschange && gaschange < 35) {
	display.drawBitmap(0, 0, epd_bitmap_4_dying, 128, 64, WHITE);
	Serial.println("Superbad");
	ledcWriteTone(0, 1000);
  	delay(300);
  }
  if (35 <= gaschange) {
	display.drawBitmap(0, 0, epd_bitmap_5_dead, 128, 64, WHITE);
	Serial.println("Skull");
	ledcWriteTone(0, 1000);
  	delay(600);
  }
  // OLED display didnt have enough room for full words like resistance, so we shortened it
  display.setCursor(65, 0);
  display.println("Gas resist");
  display.setCursor(65, 16);
  display.println(current_gas_res);
  display.setCursor(65, 32);
  display.println("Gas change:");
  display.setCursor(65, 48);
  display.println(gaschange);
  display.display();
  ledcWriteTone(0, 0);
  delay(1000);

  // should display the bird + info
}

