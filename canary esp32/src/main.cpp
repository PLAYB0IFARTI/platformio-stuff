// main functions

void setup() {
  // General sec
  Serial.begin(115200);
  // doesnt begin it if no serials not same
  while (!Serial);

  pinMode(13, INPUT);
  pinMode(14, INPUT);
  pinMode(27, INPUT);
  pinMode(26, INPUT);

  // Buzzer sec
  ledcSetup(0, 2000, 8);
  ledcAttachPin(25, 0);

  // BME sec
  Serial.println("BME test");

  // doesnt work without a BME680
  if (!bme.begin()) {
    Serial.println("could not find BME sensor");
    while (1);
  }
  
  Serial.println("Test complete");
  bme.setTemperatureOversampling(BME680_OS_8X);
  bme.setHumidityOversampling(BME680_OS_2X);
  bme.setPressureOversampling(BME680_OS_4X);
  bme.setIIRFilterSize(BME680_FILTER_SIZE_3);
  bme.setGasHeater(320, 150);


  // OLED sec
  
  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) { 
    Serial.println(F("SSD1306 allocation failed"));
    while (1); 
  }
  display.clearDisplay();
  display.setCursor(X,Y);
  display.setTextSize(1);
  display.setTextColor(SSD1306_WHITE);
  menu();

}

void loop() {
  if (digitalRead(26) == 0 || screenstate == 1) {
    info();
    screenstate = 1;
  }
  if ((digitalRead(27) == 0 || screenstate == 2)) {
    if (calibrated == 1) {
		canary();
		screenstate = 2;
	}
	else {
		display.clearDisplay();
		display.setCursor(X,Y);
		display.println("Calibrate first!");
		display.display();
		delay(1000);
		menu();
	}
  }
  if (digitalRead(14) == 0 || screenstate == 3) {
    calibrate();
	screenstate = 0;
	calibrated = 1;

  }
// menu, doesnt have its own button, but on this it has
  if (digitalRead(13) == 0 || screenstate == 0) {
	menu();
  }
}